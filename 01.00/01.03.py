# Perform Addition, Multiplication for Vectors and Matrices

import numpy as np
vector1 = np.array([1,2,3])
vector2 = np.array([4,5,6])
print("Addition Of Vector: ", vector1+vector2)
print("Multiplication Of Vector: ", vector1*vector2)

matrix1 = np.array([[1,2],[3,4]])
matrix2 = np.array([[5,6],[7,8]])

print("Addition Of Matrix: ", matrix1+matrix2)
print("Multiplication Of Matrix: ",matrix1*matrix2)
